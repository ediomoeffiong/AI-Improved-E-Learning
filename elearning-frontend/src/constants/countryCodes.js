// Helper function to get country by code
export const getCountryByCode = (code) => {
  return COUNTRY_CODES.find(country => country.code === code);
};

// Helper function to parse phone number and extract country code
export const parsePhoneNumber = (phoneNumber) => {
  if (!phoneNumber) return { countryCode: '+234', phoneNumber: '' };
  
  const matchingCountry = COUNTRY_CODES.find(country => 
    phoneNumber.startsWith(country.code)
  );
  
  if (matchingCountry) {
    return {
      countryCode: matchingCountry.code,
      phoneNumber: phoneNumber.substring(matchingCountry.code.length),
      country: matchingCountry
    };
  }
  
  return { countryCode: '+234', phoneNumber: phoneNumber };
};

// Helper function to format phone number for display
export const formatPhoneNumber = (phoneNumber) => {
  if (!phoneNumber) return '';
  
  const parsed = parsePhoneNumber(phoneNumber);
  if (parsed.country) {
    return `${parsed.country.flag} ${parsed.countryCode} ${parsed.phoneNumber}`;
  }
  
  return phoneNumber;
};
